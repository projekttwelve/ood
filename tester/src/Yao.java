package src;
public class Yao{
  int a;
  int b;
  String c;

  public Yao(int a, int b, String c){
    this.a = a;
    this.b = b;
    this.c = c;
  }

  public int getA(){
    return this.a;
  } 

  public int getB(){
    return this.b;
  } 

  public String getC(){
    return this.c;
  } 

  public void setA(int a){
    this.a = a; 
  } 

  public void setB(int b){
    this.b = b; 
  } 

  public void setC(String c){
    this.c = c; 
  } 
}
